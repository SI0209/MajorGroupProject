namespace DrivingSchoolWebsite.Models
{
    // ApplicationUser with additional custom properties
    public class ApplicationUser : IdentityUser
    {
        public string FirstName { get; set; }
        public string LastName { get; set; }
        public string IDNumber { get; set; }
        public string StreetAddress { get; set; }
        public string Suburb { get; set; }
        public string Race { get; set; }
        public string Gender { get; set; }
        public int Age { get; set; } // Calculated from IDNumber
        public string LessonCode { get; set; }
        public DateTime? LearnerLicenseIssueDate { get; set; }
        public DateTime? LearnerLicenseExpiryDate { get; set; }

        public ClaimsIdentity GenerateUserIdentity(ApplicationUserManager manager)
        {
            var userIdentity = manager.CreateIdentity(this, DefaultAuthenticationTypes.ApplicationCookie);
            return userIdentity;
        }

        public Task<ClaimsIdentity> GenerateUserIdentityAsync(ApplicationUserManager manager)
        {
            return Task.FromResult(GenerateUserIdentity(manager));
        }
    }

    // Employee class to map to tblEmployee in the database
    

